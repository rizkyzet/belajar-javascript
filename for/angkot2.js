var jmlAngkot = 10;
var angkotBeroperasi = 5;
var noAngkot = 1;

while (noAngkot <= angkotBeroperasi) {
  console.log("Angkot No." + noAngkot + " sedang beroperasi.");
  noAngkot++;
}
for (noAngkot; noAngkot <= jmlAngkot; noAngkot++) {
  console.log("Angkot No." + noAngkot + " sedang tidak beroperasi.");
}
